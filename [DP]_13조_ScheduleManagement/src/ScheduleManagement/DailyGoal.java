package ScheduleManagement;

public class DailyGoal {

	private String Name;
	private boolean CheckBK;
	
	public String getName()
	{
		return Name;
	}
	public boolean getCheckBK()
	{
		return CheckBK;
	}
	
	public void setName(String name)
	{
		Name=name;
	}
	public void setCheckBK(boolean b)
	{
		CheckBK=b;
	}
}
